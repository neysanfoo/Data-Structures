// Stack as an ADT:
// A list with the restriction that insertion and deletion can
// be performed only from one end, called the top.
// A stack is a LIFO (Last in First Out) collection.

// Operations on a Stack:
// 1) Push(x) - Insert x into the stack
// 2) Pop() - Remove the most recent item from the stack
// 3) Top() - Returns the element at top of stack
// 4) IsEmpty() - Returns true is stack is empty, false otherwise
// These 4 operations can be performed in constant time, i.e. 
// Time Complexity O(1)

// Applications of Stack
// - Function Calls / Recursion
// - Undo / Redo operation
// - Balanced Parentheses

// We can implement stacks using:
// a) Arrays
// b) Linked Lists