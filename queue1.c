// Queue as an ADT (FIFO)
// A list or colection with the restriction that insertion can only be performed
// at one end (rear) and deletion can only be performed at the other end (front).

// Operations:
// 1) Enqueue(x) or Push(x) (Insert x)
// 2) Dequeue() or Pop() (Deletion)
// 3) Front() or Peek() (Look at front element)
// 4) IsEmpty()
// These 4 operations can be performed in constant time, i.e. 
// Time Complexity O(1)

// We can implement queues using:
// a) Arrays
// b) Linked Lists